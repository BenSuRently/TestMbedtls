int main(void)
{
    int                      ret;
    mbedtls_pk_context       pubkCtx;

    fprintf(stdout, "verfiy start\n");
    mbedtls_pk_init( &pubkCtx );

    if ( (ret = mbedtls_pk_parse_public_key( &pubkCtx, pub_key, sizeof(pub_key) )) != 0 )
    {
        fprintf(stdout, " failed\n  ! mbedtls_pk_parse_public_key returned -0x%04x\n", (unsigned int) -ret );
        goto exit;
    }
    if ( (ret = mbedtls_pk_verify( &pubkCtx, MBEDTLS_MD_SHA256, hash, sizeof(hash), sig, sizeof(sig) )) != 0 )
    {
        fprintf(stdout, " failed\n  ! mbedtls_pk_verify returned -0x%04x\n", (unsigned int) -ret );
        goto exit;
    }
    fprintf(stdout, "verify success\n");

exit:
    mbedtls_pk_free( &pubkCtx );
    return ret;
}